public class arithmetic {
    public static void main(String args[]) {
        int v1 = 5, v2 = 6;
        System.out.println(v1 + v2);
        System.out.println(v1 - v2);
        System.out.println(v1 * v2);
        System.out.println(v1 / v2);
        System.out.println(v1 % v2);

        // + oprater is also used to concate the string

        String s1 = "Hello";
        String s2 = "Abhishek";

        System.out.println(s1 + " " + s2);
    }
}
