function getRandomPhotoReference(results) {
  if (!Array.isArray(results) || results.length === 0) {
    return null;
  }
  // Function to check if aspect ratio is close to 16:9
  function isValidAspectRatio(height, width) {
    const aspectRatio = height / width;
    return Math.abs(aspectRatio - 3 / 2) < 0.3;
  }
  const validPhotos = results.flatMap((place) =>
    place.photos
      ? place.photos.filter((photo) =>
          isValidAspectRatio(photo.height, photo.width)
        )
      : []
  );

  if (validPhotos.length === 0) {
    for (const place of results) {
      if (place.photos && place.photos.length > 0) {
        return place.photos[0].photo_reference;
      }
    }
    return null;
  }

  const randomPhoto =
    validPhotos[0];
  return randomPhoto.photo_reference;
}

